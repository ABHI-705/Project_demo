import pandas as pd
import numpy as np
import altair as alt
df = pd.DataFrame(
    np.random.randn(200, 3),
    columns=['a', 'b', 'c'])

c = alt.Chart(df).mark_circle().encode(
    x='a', y='b', size='c', color='c', tooltip=['a', 'b', 'c'])

st.write(c)
st.line_chart(df)
st.area_chart(df)
col1, col2, col3 = st.columns(3)

with col1:
   st.header("A cat")
